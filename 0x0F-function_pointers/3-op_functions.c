#include "3-calc.h"

/**
 * op_add - Perform simple addition operation.
 * @a: The number to be summed.
 * @b: The number to be summed.
 * Return: The sum.
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Perform simple subtraction operatin.
 * @a: The nmumber.
 * @b: The number.
 * Return: The difference of a and b.
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_div - Perform simple division operation.
 * @a: The number.
 * @b: The number.
 * Return: The division of a by b.
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mul - Perform simple multiplication operation.
 * @a: The number to multiply.
 * @b: The number to multiply.
 * Return: The product of a and b.
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_mod - Perform simple modulos operation.
 * @a: The number.
 * @b: The number.
 * Return: The remainder of the division of a by b.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
